#Q.2)Compute the standard deviation of the NumPy array

# Importing the NumPy library for numerical operations
import numpy as np

# Input array
arr = [20, 2, 7, 1, 34]

# Compute the standard deviation using the np.std function
# Standard deviation measures the amount of variation or dispersion in a set of values
stnd = np.std(arr)

# Output the calculated standard deviation
print(stnd)

"""
OUTPUT : 12.576167937809991
"""
